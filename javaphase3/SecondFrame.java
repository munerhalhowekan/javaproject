    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     // Save file button
      if( list == null ){
          JOptionPane.showMessageDialog(this, "no Course found" ); 
          return ; 
        }
        
         try {
    String fileName = list.getCourseName()+".ser";
   File out = new File(fileName);
   
   if( out.exists() ){
  int num = JOptionPane.showConfirmDialog(null , "this file is already exist do you want to continue ? " ,
         "alert", JOptionPane.YES_NO_OPTION ) ;
  if (num == JOptionPane.NO_OPTION)
      return ; 
   }
   
 FileOutputStream fos = new FileOutputStream (out);
 ObjectOutputStream file = new ObjectOutputStream (fos);
 
 file.writeObject(list );
  
 file.close() ; 
  JOptionPane.showMessageDialog(null, "save all information is done in " + fileName);
  
    }catch( IOException ex){
       JOptionPane.showMessageDialog(null,"error !!! in my project " + ex.toString() );
    }


          
      
       

    }//GEN-LAST:event_jButton3ActionPerformed


       // Load file button
     if( list != null ){
        int num = JOptionPane.showConfirmDialog(null , " there is already Course open , do you want to continue ? " ,
         "alert", JOptionPane.YES_NO_OPTION ) ;
  if (num == JOptionPane.NO_OPTION)
      return ; 
   }
       
       
      String fileName =jTextField23.getText().trim()+".ser";
       try{
  File f = new File( fileName ) ; 
  if( ! f.exists() ){
   JOptionPane.showMessageDialog(this,"file is not exist");
        return ;   
  }
  FileInputStream fi =  new FileInputStream(f) ;
  ObjectInputStream in= new ObjectInputStream( fi) ; 
 
 list = (Course)in.readObject() ; 
   JOptionPane.showMessageDialog(this,  "load Course is done " ) ; 
 in.close() ;
 }
 catch(ClassNotFoundException ex){
      JOptionPane.showMessageDialog(null, "error while reading object ");
         }
